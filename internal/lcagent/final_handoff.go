const (
	finalHandoffTranscriptMaxChars = 80000
	finalHandoffAssistantMaxChars  = 2400
	finalHandoffArgsMaxChars       = 1100
	finalHandoffToolOutputChars    = 1800
	finalHandoffRecentOutputChars  = 5000
	finalHandoffRecentToolResults  = 8

	loopCompactionCharThreshold   = 200000
	loopCompactionTranscriptChars = 50000
	loopCompactionAssistantChars  = 1600
	loopCompactionArgsChars       = 800
	loopCompactionToolOutputChars = 900
	loopCompactionRecentChars     = 2500
	loopCompactionRecentResults   = 4

	loopCompactedContextPrefix = "Compacted session context generated by the harness."
)

type openRouterContextProfile string

const (
	openRouterContextProfileBalanced openRouterContextProfile = "balanced"
	openRouterContextProfileLarge    openRouterContextProfile = "large"
)

type finalHandoffCompactionStats struct {
	OriginalMessages  int `json:"original_messages"`
	CompactedMessages int `json:"compacted_messages"`
	OriginalChars     int `json:"original_chars"`
	CompactedChars    int `json:"compacted_chars"`
	ToolResults       int `json:"tool_results"`
	ReadLedgerFiles   int `json:"read_ledger_files,omitempty"`
	ReadLedgerRanges  int `json:"read_ledger_ranges,omitempty"`
}

type compactToolCall struct {
	Name string
	Args string
}

type openRouterCompactionOptions struct {
	TranscriptMaxChars int
	AssistantMaxChars  int
	ArgsMaxChars       int
	ToolOutputChars    int
	RecentOutputChars  int
	RecentToolResults  int
}

type openRouterContextOptions struct {
	FinalHandoffTranscriptMaxChars int `json:"final_handoff_transcript_max_chars"`
	FinalHandoffAssistantMaxChars  int `json:"final_handoff_assistant_max_chars"`
	FinalHandoffArgsMaxChars       int `json:"final_handoff_args_max_chars"`
	FinalHandoffToolOutputChars    int `json:"final_handoff_tool_output_chars"`
	FinalHandoffRecentOutputChars  int `json:"final_handoff_recent_output_chars"`
	FinalHandoffRecentToolResults  int `json:"final_handoff_recent_tool_results"`
	LoopCompactionCharThreshold    int `json:"loop_compaction_char_threshold"`
	LoopCompactionTranscriptChars  int `json:"loop_compaction_transcript_chars"`
	LoopCompactionAssistantChars   int `json:"loop_compaction_assistant_chars"`
	LoopCompactionArgsChars        int `json:"loop_compaction_args_chars"`
	LoopCompactionToolOutputChars  int `json:"loop_compaction_tool_output_chars"`
	LoopCompactionRecentChars      int `json:"loop_compaction_recent_chars"`
	LoopCompactionRecentResults    int `json:"loop_compaction_recent_results"`
}

func parseOpenRouterContextProfile(raw string) (openRouterContextProfile, error) {
	switch openRouterContextProfile(strings.ToLower(strings.TrimSpace(raw))) {
	case "", openRouterContextProfileBalanced:
		return openRouterContextProfileBalanced, nil
	case openRouterContextProfileLarge:
		return openRouterContextProfileLarge, nil
	default:
		return "", fmt.Errorf("unsupported context profile: %s", raw)
	}
}

